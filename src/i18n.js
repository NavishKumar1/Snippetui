let currentLang = localStorage.getItem('sui_lang') || 'en';

export function getCurrentLanguage() {
  return currentLang;
}

export function setLanguage(lang) {
  if (LANGUAGES[lang]) {
    currentLang = lang;
    localStorage.setItem('sui_lang', lang);
    document.documentElement.lang = lang;
    
    // Dispatch custom event to notify dynamic views to update
    window.dispatchEvent(new CustomEvent('languagechanged', { detail: { lang } }));
  }
}

export function t(key, replacements = {}) {
  let text = key;
  if (TRANSLATIONS[currentLang] && TRANSLATIONS[currentLang][key]) {
    text = TRANSLATIONS[currentLang][key];
  } else if (TRANSLATIONS.en[key]) {
    text = TRANSLATIONS.en[key];
  }
  
  // Apply placeholders (e.g. {query} -> replacement)
  Object.keys(replacements).forEach(placeholder => {
    text = text.replace(`{${placeholder}}`, replacements[placeholder]);
  });
  
  return text;
}
